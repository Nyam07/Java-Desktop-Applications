    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // CODE FOR SEARCH BUTTON
        String regNo = jTextField1.getText();
        
        //get date
        SimpleDateFormat sdf = new SimpleDateFormat("MMM-yyyy");
        Date date = new Date();
        //assign date gotten to month
        String month = sdf.format(date);
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from students where RegNo = '"+regNo+"' and Status = 'Living'");
            
            if(rs.next()){
                jTextField1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(5));
                jTextField4.setText(rs.getString(9));
                jTextField5.setText(month);
                jTextField6.setText("6000");
            }
            else{
                JOptionPane.showMessageDialog(null, "Student does not exist");
                clear();
            }
            
             tableDetails();
             
             //check whether student has paid fees already
             ResultSet rs1 = st.executeQuery("Select * from fees inner join students where students.Status ='living' and fees.Month ='"+month+"' and fees.RegNo ='"+regNo+"' and students.RegNo = '"+regNo+"'");
             
             //if student already exists and has completed fees then display message and hide save button
             if(rs1.next()){
                 String amountPaid;
                 String balance;
                 ResultSet rs2 = st.executeQuery("Select * from fees where RegNo = '"+regNo+ "'");
                 if(rs2.next()){
                     amountPaid = rs2.getString(4);
                     balance = rs2.getString(5);
                     
                     //display in text boxes
                     jTextField7.setText(amountPaid);
                     jTextField8.setText(balance);
                     
                     //set other text fields to be inactive
                     jTextField2.setEditable(false);
                     jTextField3.setEditable(false);
                     jTextField4.setEditable(false);
                     jTextField5.setEditable(false);
                     
                     //if balance = 0
                     if(balance.equals("0")){
                       jButton3.setVisible(false);
                       JOptionPane.showMessageDialog(null, "The fees for this month is already completed");
                     }
                 }
                   
             }
            
        }catch(Exception e){
          JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // CODE FOR SAVE BUTTON
        String RegNo = jTextField1.getText();
        String Month = jTextField5.getText();
        int rent = Integer.parseInt(jTextField6.getText());
        int amountPaid = Integer.parseInt(jTextField7.getText());
        int balance = calculateBalance(rent, amountPaid);
        
        try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("Insert into fees values (?, ?, ?, ?, ?)");
            ps.setString(1, RegNo);
            ps.setString(2, Month);
            ps.setInt(3, rent);
            ps.setInt(4, amountPaid);
            ps.setInt(5, balance);
            
            
            ps.executeUpdate();
            tableDetails();
            JOptionPane.showMessageDialog(null, "Successfully saved");
            clear();
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(null, "Error!! Student already paid!! Please update the fee or check the RegNo");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

