    //create a method to display available rooms in the checkbox
    public void roomNumber(){
        int i = 0;
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from room where Active = 'Yes' and RoomStatus = 'Not Booked' " );
            
            while(rs.next()){
                i = 1;   //shows that a room is available
                jComboBox1.addItem(rs.getString(1));   //jcombobox will have values of the room number since it is the first column(1)
            }
            
            if(i == 0){
                jButton2.setVisible(false);
                JOptionPane.showMessageDialog(null, "All rooms are currently booked");
                setVisible(false);
            }
            
        }
        catch(Exception e){
            
        }
    }
