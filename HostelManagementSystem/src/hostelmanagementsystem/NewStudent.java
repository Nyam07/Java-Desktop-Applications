    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // CODE FOR SAVE BUTTON
        String regNo = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox2.getSelectedItem();
        String parentContact = jTextField4.getText();
        String email = jTextField5.getText();
        String address = jTextField6.getText();
        String college = jTextField7.getText();
        String contact = jTextField8.getText();
        String room = (String)jComboBox1.getSelectedItem();
        String status = "Living";
        
        try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("Insert into students values (?, ?, ?, ?, ?,?, ?, ?, ?, ?)");
            ps.setString(1, regNo);
            ps.setString(2, name);
            ps.setString(3, gender);
            ps.setString(4, parentContact);
            ps.setString(5, email);
            ps.setString(6, address);
            ps.setString(7, college);
            ps.setString(8, contact);
            ps.setString(9, room);
            ps.setString(10, status);
            
            ps.executeUpdate();
            
            //update the room to be booked
            PreparedStatement ps1 = con.prepareStatement("Update room set RoomStatus = 'Booked' where Number = ?");
            ps1.setString(1, room);
            ps1.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            clear();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

