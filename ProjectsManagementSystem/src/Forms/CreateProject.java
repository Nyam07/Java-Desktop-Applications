    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // CODE FOR SAVE BUTTON in programming page
        //create a simple date format for setting format of the date
        SimpleDateFormat sdf = new SimpleDateFormat("yyy-MM-dd", Locale.getDefault());
        
        String projectId = jTextField1.getText();
        String projectName = jTextField2.getText();
        String projectType = jTextField8.getText();
        String language = jTextField3.getText();
        String dateofCreation = sdf.format(jDateChooser2.getDate());
        String dateDue = sdf.format(jDateChooser1.getDate());
        String dateCompleted = null;
        int cost = Integer.parseInt(jTextField4.getText());
        int paid = Integer.parseInt(jTextField5.getText());
        int balance = cost - paid;
        String clientId = (String)jComboBox1.getSelectedItem();
        String description = jTextArea1.getText();
        String projectStatus = "New";
        
        try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("Insert into programming values (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");
            
            ps.setString(1, projectId);
            ps.setString(2, projectName);
            ps.setString(3, projectType);
            ps.setString(4, language);
            ps.setString(5, dateofCreation);
            ps.setString(6, dateDue);
            ps.setString(7, dateCompleted);
            ps.setInt(8, cost);
            ps.setInt(9, paid);
            ps.setInt(10, balance);
            ps.setString(11, clientId);
            ps.setString(12, description);
            ps.setString(13, projectStatus);
            
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successfully saved");
            setVisible(false);
            new CreateProject().setVisible(true);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

