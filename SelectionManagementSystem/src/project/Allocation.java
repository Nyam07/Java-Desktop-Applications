    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // CODE FOR ALLOCATE BUTTON
        String indexNumber, national, extra, county;
        int marks = 0;
        String Allocated;
         //get all students from selection table
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from selection");
            
            while(rs.next()){
                //allocate strings to the values from the database
                indexNumber = rs.getString(1);
                national = rs.getString(2);
                extra = rs.getString(3);
                county = rs.getString(4);
                 //gets marks of the current student
                try{
                Connection con1 = ConnectionProvider.getCon();
                Statement st1 = con1.createStatement();
                ResultSet rs1 = st1.executeQuery("Select Marks from students where IndexNo = '"+indexNumber+"'");
                if(rs1.next())
                
                    marks = rs1.getInt(1);
                
                if (marks >= 400){
                    Allocated = national;
                }
                else if (marks >= 300){
                    Allocated = extra;
                }
                else
                    Allocated = county;
                
                //Update the student allocation 
               try{
                   Connection con2 = ConnectionProvider.getCon();
                   Statement st2 = con2.createStatement();
                   st2.executeUpdate("Update students set Allocation = '"+Allocated+ "' where IndexNo = '"+indexNumber+"'");
                   
                   
               }catch(Exception e){
                   JOptionPane.showMessageDialog(null, e);
               }
                
                
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
                
            }
            JOptionPane.showMessageDialog(null, "Allocation Successfull");
            tableDetails();
            checkAllocation();
            
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Failed at first query");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

