    public void tableDetails(){
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select IndexNo, Name, Marks, Allocation from students");
            
            while(rs.next()){
                dtm.addRow(new Object[] {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    //method set label to be not visible until it detects unallocated student
    public void checkAllocation(){
      try{
         Connection con = ConnectionProvider.getCon();
         Statement st = con.createStatement();
         ResultSet rs = st.executeQuery("Select Allocation from students");
         
         while(rs.next()){
             String alloc = rs.getString(1);
             if(alloc.equals("None")){
                 jLabel1.setVisible(true);
                 break;
             }
             else{
                 jLabel1.setVisible(false);
                 continue;
             }
         }
      }  catch(Exception e){
          JOptionPane.showMessageDialog(null, e);
      }
    }
