    //Method to add text after selecting school
public void getSchoolName(JComboBox combo, JTextField text){
    // CODE TO SHOW NAME OF SELECTED SCHOOL
        String id = (String)combo.getSelectedItem();
        try{
           Connection con = ConnectionProvider.getCon();
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("Select Name from schools where schoolId = '"+ id + "'");
           
           if(rs.next())
               text.setText(rs.getString("Name"));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

}
    //create a function to input school ids into the comboboxes
public void getSchools(String query, JComboBox combo){
    
    try{
    Connection con = ConnectionProvider.getCon();
    Statement st = con.createStatement();
    ResultSet rs = st.executeQuery(query);
    
    while(rs.next()){
        combo.addItem(rs.getString(1));
    }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }
}
    
    //method to insert details in table
     public void tableDetails(){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from selection");
            
            while(rs.next()){
                dtm.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});
            }
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
     }
    
