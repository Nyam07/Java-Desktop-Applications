    private void jComboBox4ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox4ItemStateChanged
       //SELECT SCHOOL ACCORDING TO YOUR GENDER
       String stGender = null;
       String index = (String)jComboBox4.getSelectedItem();
       try{
           Connection con = ConnectionProvider.getCon();
           Statement st = con.createStatement();
           ResultSet rs = st.executeQuery("Select Gender from students where IndexNo = '"+index+"'");
           
           if(rs.next()){
               stGender = rs.getString("Gender");
           }
           
       String nQuery = "Select schoolId from schools where Type = 'National' AND Gender = '"+ stGender +"' OR Type = 'National' AND Gender = 'Mixed'";      
       String eQuery = "Select schoolId from schools where Type = 'County' AND Gender = '"+ stGender +"' OR Type = 'County' AND Gender = 'Mixed'";
       String dQuery = "Select schoolId from schools where Type = 'District' AND Gender = '"+ stGender +"' OR Type = 'District' AND Gender = 'Mixed'";
       
       getSchools(nQuery, jComboBox1);
       getSchools(eQuery, jComboBox2);
       getSchools(dQuery, jComboBox3);
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
      
    }//GEN-LAST:event_jComboBox4ItemStateChanged

