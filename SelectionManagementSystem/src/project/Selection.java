    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // CODE FOR SAVE BUTTON
        String indexNo =(String)jComboBox4.getSelectedItem();
        String national =(String)jComboBox1.getSelectedItem();
        String county =(String)jComboBox2.getSelectedItem();
        String district =(String)jComboBox3.getSelectedItem();
        
        try{
            Connection con = ConnectionProvider.getCon();
            PreparedStatement st = con.prepareStatement("Insert into selection values(?, ?, ?, ?)");
            st.setString(1, indexNo);
            st.setString(2, national);
            st.setString(3, county);
            st.setString(4, district);
            
            st.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successfully saved!");
            tableDetails();
            setVisible(false);
            new Selection().setVisible(true);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

